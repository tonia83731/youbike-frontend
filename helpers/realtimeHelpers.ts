export const mergeRealtimeData = (stationInfo: any[], capacityInfo: any[]) => {
  return stationInfo.map((item) => {
    const capacity = capacityInfo.find(
      (data) => data.StationUID === item.StationUID
    );

    return {
      StationUID: item.StationUID,
      StationName: item.StationName,
      StationPosition: item.StationPosition,
      StationAddress: item.StationAddress,
      AvailableRentBikes: capacity ? capacity.AvailableRentBikes : null,
      AvailableReturnBikes: capacity ? capacity.AvailableReturnBikes : null,
      AvailableRentBikesDetail: capacity
        ? capacity.AvailableRentBikesDetail
        : null,
    };
  });
};

export const mergeRealtimeTableData = (
  stationInfo: any[],
  capacityInfo: any[]
) => {
  return stationInfo.map((item) => {
    const capacity = capacityInfo.find(
      (data) => data.StationUID === item.StationUID
    );
    const name = item.StationName.Zh_tw.split("_")[1];
    return {
      StationName: name,
      StationPosition: item.StationPosition,
      StationAddress: item.StationAddress.Zh_tw,
      AvailableRenGenerBikes: capacity
        ? capacity.AvailableRentBikesDetail.GeneralBikes
        : null,
      AvailableRentElecBikes: capacity
        ? capacity.AvailableRentBikesDetail.ElectricBikes
        : null,
      AvailableReturnBikes: capacity ? capacity.AvailableReturnBikes : null,
    };
  });
};
